public class Tables {
    public static void main(String[] args) {
        
        int n=100;

        for(int i=1;i<=n;i++){
            for(int j=1;j<=10;j++){
                //    System.out.printf("%4d", i * j);
                System.out.println(" "+i+" * "+j+" = "+i*j);
            }
            System.out.println();
        }
    }
}
